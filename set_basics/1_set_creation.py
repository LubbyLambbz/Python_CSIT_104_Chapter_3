# Create a set named 'nums1' using the set() function
# A set is like a bag that only keeps unique items and does not allow duplicates

print()
nums1 = set([1, 2, 3])

# Create a set named 'nums2' using a set literal (curly braces {})
nums2 = {7, 8, 9}

# Print the contents of the first set 'nums1'
print(f'Contents of nums1 set: {nums1}\n')  # Output: {1, 2, 3}

# Print the contents of the second set 'nums2'
print(f'Contents of nums2 set: {nums2}\n')  # Output: {7, 8, 9}
