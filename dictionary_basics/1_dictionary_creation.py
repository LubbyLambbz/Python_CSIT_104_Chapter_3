# Create a dictionary named 'players' to store soccer players and their jersey numbers
# A dictionary is a collection that stores data in key-value pairs, where each key is a unique identifier
# and each value represents some information associated with that key

print()

players = {  
    'Lionel Messi': 10,  # The key 'Lionel Messi' represents the player, and the value '10' is his jersey number
    'Cristiano Ronaldo': 7  # The key 'Cristiano Ronaldo' represents another player, and the value '7' is his jersey number
}

# Print the dictionary to show the player names and their jersey numbers
print(f'Players and their jersey numbers: {players}\n')  
# Output: {'Lionel Messi': 10, 'Cristiano Ronaldo': 7}
